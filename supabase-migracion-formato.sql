  ALTER COLUMN "DES"    TYPE BIGINT USING ROUND(COALESCE("DES",0))::bigint,
  ALTER COLUMN "TOTAL"  TYPE BIGINT USING ROUND(COALESCE("TOTAL",0))::bigint,
  ALTER COLUMN "UTIL"   TYPE BIGINT USING ROUND(COALESCE("UTIL",0))::bigint;

ALTER TABLE public."INVENTARIO"
  ALTER COLUMN "VR PR"  SET DEFAULT 0,
  ALTER COLUMN "VR RT"  SET DEFAULT 0,
  ALTER COLUMN "VR IN"  SET DEFAULT 0,
  ALTER COLUMN "TOT IN" SET DEFAULT 0,
  ALTER COLUMN "VR AB"  SET DEFAULT 0,
  ALTER COLUMN "DES"    SET DEFAULT 0,
  ALTER COLUMN "TOTAL"  SET DEFAULT 0,
  ALTER COLUMN "UTIL"   SET DEFAULT 0;

-- -------------------------------------------------------------
-- 4) CAJA
-- -------------------------------------------------------------
ALTER TABLE public."CAJA"
  ALTER COLUMN "FECHA"     TYPE TIMESTAMP(0) WITH TIME ZONE,
  ALTER COLUMN "BASE"      TYPE BIGINT USING ROUND(COALESCE("BASE",0))::bigint,
  ALTER COLUMN "RETIRO"    TYPE BIGINT USING ROUND(COALESCE("RETIRO",0))::bigint,
  ALTER COLUMN "GUARDA"    TYPE BIGINT USING ROUND(COALESCE("GUARDA",0))::bigint,
  ALTER COLUMN "BAR"       TYPE BIGINT USING ROUND(COALESCE("BAR",0))::bigint,
  ALTER COLUMN "CONTRATOS" TYPE BIGINT USING ROUND(COALESCE("CONTRATOS",0))::bigint,
  ALTER COLUMN "GASTOS"    TYPE BIGINT USING ROUND(COALESCE("GASTOS",0))::bigint,
  ALTER COLUMN "VENTAS"    TYPE BIGINT USING ROUND(COALESCE("VENTAS",0))::bigint;

ALTER TABLE public."CAJA"
  ALTER COLUMN "BASE"      SET DEFAULT 0,
  ALTER COLUMN "RETIRO"    SET DEFAULT 0,
  ALTER COLUMN "GUARDA"    SET DEFAULT 0,
  ALTER COLUMN "BAR"       SET DEFAULT 0,
  ALTER COLUMN "CONTRATOS" SET DEFAULT 0,
  ALTER COLUMN "GASTOS"    SET DEFAULT 0,
  ALTER COLUMN "VENTAS"    SET DEFAULT 0;

ALTER TABLE public."CAJA"
  ADD COLUMN "CUADRE CAJA" BIGINT GENERATED ALWAYS AS (
    COALESCE("BASE",0) + COALESCE("RETIRO",0) + COALESCE("GUARDA",0) +
    COALESCE("BAR",0) + COALESCE("VENTAS",0)
    - COALESCE("CONTRATOS",0) - COALESCE("GASTOS",0)
  ) STORED;

