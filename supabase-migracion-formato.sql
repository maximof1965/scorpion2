  "FECHA","OPER","NUMERO","VR PR","VR RT","NOMBRE","CC","ART",
  "DESCRIPCION","LLEVAR","VR IN","TOT IN","FECHA INT","VR AB",
  "FECHA ABON","ESPERA","RETIROS","FECHA RETIRO","DES","AUMENTO",
  "FECHA AU","TOTAL","UTIL","VALOR VENTA","FECHA VENTA"
