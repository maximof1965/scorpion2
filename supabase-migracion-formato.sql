  ALTER COLUMN "FECHA ABON"   TYPE TIMESTAMP(0) WITH TIME ZONE,
  ALTER COLUMN "FECHA RETIRO" TYPE TIMESTAMP(0) WITH TIME ZONE,
  ALTER COLUMN "FECHA AU"     TYPE TIMESTAMP(0) WITH TIME ZONE,
  ALTER COLUMN "VR PR"  TYPE BIGINT USING ROUND(COALESCE("VR PR",0))::bigint,
  ALTER COLUMN "VR RT"  TYPE BIGINT USING ROUND(COALESCE("VR RT",0))::bigint,
