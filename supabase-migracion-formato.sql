CREATE VIEW public."VW_INVENTARIO" AS
SELECT
  "FECHA","OPER","NUMERO","VR PR","VR RT","NOMBRE","CC","ART",
  "DESCRIPCION","LLEVAR","VR IN","TOT IN","FECHA INT","VR AB",
  "FECHA ABON","ESPERA","RETIROS","FECHA RETIRO","DES","AUMENTO",
  "FECHA AU","TOTAL","UTIL"
FROM public."INVENTARIO";

CREATE VIEW public."VW_CAJA" AS
SELECT
  "FECHA","TURNO","BASE","RETIRO","GUARDA","BAR","CONTRATOS",
  "GASTOS","VENTAS","CUADRE CAJA"
FROM public."CAJA";

CREATE VIEW public."VW_RETIROS" AS
SELECT
  "FECHA","OPER","NUMERO","VR PR","VR RT","NOMBRE","CC","ART",
  "DESCRIPCION","LLEVAR","VR IN","TOT IN","FECHA INT","VR AB",
  "FECHA ABON","ESPERA","RETIROS","FECHA RETIRO","DES","AUMENTO",
  "FECHA AU","TOTAL","UTIL"
