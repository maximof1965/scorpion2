-- -------------------------------------------------------------
-- TABLAS NUEVAS
-- -------------------------------------------------------------
CREATE TABLE public."INVENTARIO" (
  "FECHA" TIMESTAMP(0) WITH TIME ZONE NOT NULL,
  "OPER" TEXT,
  "NUMERO" INTEGER NOT NULL CHECK ("NUMERO" BETWEEN 100000 AND 999999),
  "VR PR" BIGINT DEFAULT 0,
  "VR RT" BIGINT DEFAULT 0,
  "NOMBRE" TEXT,
  "CC" TEXT,
  "ART" TEXT,
  "DESCRIPCION" TEXT,
  "LLEVAR" TEXT,
  "VR IN" BIGINT DEFAULT 0,
  "TOT IN" BIGINT DEFAULT 0,
  "FECHA INT" TIMESTAMP(0) WITH TIME ZONE,
  "VR AB" BIGINT DEFAULT 0,
  "FECHA ABON" TIMESTAMP(0) WITH TIME ZONE,
  "ESPERA" TEXT,
  "RETIROS" TEXT,
  "FECHA RETIRO" TIMESTAMP(0) WITH TIME ZONE,
  "DES" BIGINT DEFAULT 0,
  "AUMENTO" TEXT,
  "FECHA AU" TIMESTAMP(0) WITH TIME ZONE,
  "TOTAL" BIGINT DEFAULT 0,
  "UTIL" BIGINT DEFAULT 0,
  CONSTRAINT "PK_INVENTARIO" PRIMARY KEY ("NUMERO")
);

CREATE TABLE public."CAJA" (
  "FECHA" TIMESTAMP(0) WITH TIME ZONE NOT NULL,
  "TURNO" TEXT NOT NULL,
  "BASE" BIGINT DEFAULT 0,
  "RETIRO" BIGINT DEFAULT 0,
  "GUARDA" BIGINT DEFAULT 0,
  "BAR" BIGINT DEFAULT 0,
  "CONTRATOS" BIGINT DEFAULT 0,
  "GASTOS" BIGINT DEFAULT 0,
  "VENTAS" BIGINT DEFAULT 0,
  "CUADRE CAJA" BIGINT GENERATED ALWAYS AS (
    COALESCE("BASE", 0) + COALESCE("RETIRO", 0) + COALESCE("GUARDA", 0) +
    COALESCE("BAR", 0) + COALESCE("VENTAS", 0)
    - COALESCE("CONTRATOS", 0) - COALESCE("GASTOS", 0)
  ) STORED
);

CREATE TABLE public."RETIROS" (
  "FECHA" TIMESTAMP(0) WITH TIME ZONE NOT NULL,
  "OPER" TEXT,
