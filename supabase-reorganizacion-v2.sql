  "CC" TEXT,
  "ART" TEXT,
  "DESCRIPCION" TEXT,
  "LLEVAR" TEXT,
  "VR IN" BIGINT DEFAULT 0,
  "TOT IN" BIGINT DEFAULT 0,
  "FECHA INT" TIMESTAMP(0) WITH TIME ZONE,
  "VR AB" BIGINT DEFAULT 0,
  "FECHA ABON" TIMESTAMP(0) WITH TIME ZONE,
  "ESPERA" TEXT,
  "RETIROS" TEXT,
